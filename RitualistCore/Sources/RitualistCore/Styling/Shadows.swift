import Foundation

public enum ShadowTokens {
    // Light shadows
    public static let lightRadius: CGFloat = 2
    public static let lightOffset = (width: 0.0, height: 1.0)
    
    // Medium shadows  
    public static let mediumRadius: CGFloat = 4
    public static let mediumOffset = (width: 0.0, height: 2.0)
    
    // Large shadows
    public static let largeRadius: CGFloat = 10
    public static let largeOffset = (width: 0.0, height: 5.0)
}